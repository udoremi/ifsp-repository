/*
nesse programa vamos criar nossa primeira variável.
A variável é um espaço na memória do computador
que permite armazenamento de dados
*/
#include <iostream>
using namespace std;
int main(){
	
	
	// normalmente as variáveis são criadas no início, logo após a função main
	// devem ser obrigatoriamente criadas antes de serem usadas
	// vamos criar uma variável do tipó numeral inteiro
	int numero;
	
	// vamos atribuir um valor para ela
	numero=25;
	cout<<"O valor da variável número eh: "<<numero;
	
	return 0;
}
